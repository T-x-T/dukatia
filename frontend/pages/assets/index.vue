<template>
	<div>
		<button class="green" @click="newAsset">Add</button>
		<CustomTable
			v-if="tableData"
			:tableData="tableData"
			v-on:rowClick="rowClick"
		/>
	</div>
</template>

<script lang="ts" setup>
const assets: any = (await useFetch("/api/v1/assets/all")).data.value;
const currencies: any = (await useFetch("/api/v1/currencies/all")).data.value;
const tags: any = (await useFetch("/api/v1/tags/all")).data.value;

const assetsForDisplay = assets.map((x: any) => {
	x.currency = currencies.filter((c: any) => c.id == x.currency_id)[0];
	return x;
});

const tableData = {
	multiSelect: false,
	displaySum: true,
	sumColumn: 5,
	defaultSort: {
		column: 5,
		sort: "desc"
	},
	columns: [
		{name: "ID", type: "number"},
		{name: "Name", type: "string"},
		{name: "Description", type: "string"},
		{name: "Amount", type: "number"},
		{name: "Value per Unit", type: "number"},
		{name: "Total value", type: "number"},
		{name: "Tags", type: "choice", options: [...new Set(tags.map((x: any) => x.name))]}
	],
	rows: assetsForDisplay.map((x: any) => ([
		x.id,
		x.name,
		x.description,
		Math.round(x.amount * 10000 + Number.EPSILON) / 10000,
		`${x.value_per_unit / x.currency.minor_in_mayor}${x.currency.symbol}`,
		`${Math.round(((x.amount * x.value_per_unit) / x.currency.minor_in_mayor) * 100 + Number.EPSILON) / 100}${x.currency.symbol}`,
		tags.filter((y: any) => x.tag_ids?.includes(y.id)).map((y: any) => y.name).join(", ")
	]))
};

async function rowClick(row: any) {
	await useRouter().push(`/assets/${row[0]}`);
};

async function newAsset() {
	await useRouter().push("/assets/new");
};
</script>